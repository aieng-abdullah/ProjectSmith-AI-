"use client";

import { useState } from "react";
import {
  FileText,
  DollarSign,
  AlertTriangle,
  Download,
  ClipboardList,
} from "lucide-react";
import { Button } from "@/components/ui/button";
import { Separator } from "@/components/ui/separator";
import { cn } from "@/lib/utils";

interface PlanResultProps {
  result: {
    plan: string;
    cost: string;
    edges: string;
    prd: string;
  };
}

type Tab = "plan" | "cost" | "edges" | "prd";

export function PlanResult({ result }: PlanResultProps) {
  const [activeTab, setActiveTab] = useState<Tab>("plan");

  const tabs: { id: Tab; label: string; icon: React.ElementType }[] = [
    { id: "plan", label: "Project Plan", icon: ClipboardList },
    { id: "cost", label: "Cost & Stack", icon: DollarSign },
    { id: "edges", label: "Edge Cases", icon: AlertTriangle },
    { id: "prd", label: "Project Brief", icon: FileText },
  ];

  const handleDownloadMarkdown = () => {
    const content = `# Project Brief

## Project Plan
${result.plan}

## Cost & Stack
${result.cost}

## Edge Cases & Risks
${result.edges}

## Full Brief
${result.prd}

---
*Generated by ProjectSmith AI - Always validate with real experts before taking action.*
`;

    const blob = new Blob([content], { type: "text/markdown" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = "project-brief.md";
    a.click();
    URL.revokeObjectURL(url);
  };

  const getContent = () => {
    switch (activeTab) {
      case "plan":
        return result.plan || "No plan data available.";
      case "cost":
        return result.cost || "No cost data available.";
      case "edges":
        return result.edges || "No edge cases available.";
      case "prd":
        return result.prd || "No project brief available.";
    }
  };

  return (
    <div className="mt-8 rounded-xl border border-border bg-card overflow-hidden">
      {/* Header */}
      <div className="flex items-center justify-between px-4 py-3 bg-muted/30 border-b border-border">
        <h3 className="font-semibold text-foreground">Your Project Plan</h3>
        <Button
          variant="outline"
          size="sm"
          onClick={handleDownloadMarkdown}
          className="gap-2"
        >
          <Download className="h-4 w-4" />
          <span className="hidden sm:inline">Download</span>
        </Button>
      </div>

      {/* Tabs */}
      <div className="flex border-b border-border overflow-x-auto">
        {tabs.map((tab) => {
          const Icon = tab.icon;
          return (
            <button
              key={tab.id}
              onClick={() => setActiveTab(tab.id)}
              className={cn(
                "flex items-center gap-2 px-4 py-3 text-sm font-medium transition-colors whitespace-nowrap",
                activeTab === tab.id
                  ? "text-foreground border-b-2 border-primary"
                  : "text-muted-foreground hover:text-foreground"
              )}
            >
              <Icon className="h-4 w-4" />
              {tab.label}
            </button>
          );
        })}
      </div>

      {/* Content */}
      <div className="p-6">
        <div className="prose prose-sm prose-invert max-w-none">
          <div className="whitespace-pre-wrap text-foreground leading-relaxed">
            {getContent()}
          </div>
        </div>
      </div>

      {/* Footer */}
      <div className="px-4 py-3 bg-muted/30 border-t border-border">
        <p className="text-xs text-muted-foreground text-center">
          AI-generated plan. Always validate with qualified professionals
          before making business decisions.
        </p>
      </div>
    </div>
  );
}
